package com.lxl.common.models;

import java.util.Date;

public class HouseHold {
    private Integer houseHoldId;
    private Integer houseHoldRoomId;
    private String houseHoldName;
    private String houseHoldPhone;
    private String houseHoldSex;
    private String houseHoldIndividual;
    private Integer houseHoldType;
    private String houseHoldDescription;
    private Date houseHoldCreateAt;
    private Date houseHoldUpdateAt;

    public Integer getHouseHoldId() {
        return houseHoldId;
    }

    public void setHouseHoldId(Integer houseHoldId) {
        this.houseHoldId = houseHoldId;
    }

    public Integer getHouseHoldRoomId() {
        return houseHoldRoomId;
    }

    public void setHouseHoldRoomId(Integer houseHoldRoomId) {
        this.houseHoldRoomId = houseHoldRoomId;
    }

    public String getHouseHoldName() {
        return houseHoldName;
    }

    public void setHouseHoldName(String houseHoldName) {
        this.houseHoldName = houseHoldName;
    }

    public String getHouseHoldPhone() {
        return houseHoldPhone;
    }

    public void setHouseHoldPhone(String houseHoldPhone) {
        this.houseHoldPhone = houseHoldPhone;
    }

    public String getHouseHoldSex() {
        return houseHoldSex;
    }

    public void setHouseHoldSex(String houseHoldSex) {
        this.houseHoldSex = houseHoldSex;
    }

    public String getHouseHoldIndividual() {
        return houseHoldIndividual;
    }

    public void setHouseHoldIndividual(String houseHoldIndividual) {
        this.houseHoldIndividual = houseHoldIndividual;
    }

    public Integer getHouseHoldType() {
        return houseHoldType;
    }

    public void setHouseHoldType(Integer houseHoldType) {
        this.houseHoldType = houseHoldType;
    }

    public String getHouseHoldDescription() {
        return houseHoldDescription;
    }

    public void setHouseHoldDescription(String houseHoldDescription) {
        this.houseHoldDescription = houseHoldDescription;
    }

    public Date getHouseHoldCreateAt() {
        return houseHoldCreateAt;
    }

    public void setHouseHoldCreateAt(Date houseHoldCreateAt) {
        this.houseHoldCreateAt = houseHoldCreateAt;
    }

    public Date getHouseHoldUpdateAt() {
        return houseHoldUpdateAt;
    }

    public void setHouseHoldUpdateAt(Date houseHoldUpdateAt) {
        this.houseHoldUpdateAt = houseHoldUpdateAt;
    }
}
